/* This program is used to show perform all arthematic operations*/ 
#include <stdio.h>
int main()
{
    int a, b, c, d, e, f;
	scanf("%d%d", &a,&b);
	c=a+b;
	d=a-b;
	e=a*b;
	f=a/b;
	printf("the sum of two numbers is %d \n",c);
	printf("the difference of two numbers is %d \n",d);
	printf("the multiplication of two numbers is %d \n",e);
	printf("the division of two numbers is %d \n",f);
	return 0;
}
